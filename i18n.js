  /* ─────────────────────────────────────────────────────────────
     LOGICA
  ───────────────────────────────────────────────────────────── */
  function detectBrowserLang() {
    var nav = (navigator.language || navigator.userLanguage || 'it').toLowerCase();
    if (nav.startsWith('it')) return 'it';
    if (nav.startsWith('es')) return 'es';
    return 'en';
  }
  var currentLang = localStorage.getItem('fashionai_lang') || detectBrowserLang();

  function t(key) {
    var lang = T[currentLang] || T['it'];
    return lang[key] !== undefined ? lang[key] : (T['it'][key] !== undefined ? T['it'][key] : key);
  }

  function applyTranslations() {
    var lang = T[currentLang] || T['it'];
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (lang[key] !== undefined) el.innerHTML = lang[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (lang[key] !== undefined) el.placeholder = lang[key];
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-aria');
      if (lang[key] !== undefined) el.setAttribute('aria-label', lang[key]);
    });
    document.documentElement.lang = currentLang;
  }

  var FLAGS = { it: '🇮🇹', en: '🇬🇧', es: '🇪🇸' };

  function updateLangSwitchers() {
    var flag = FLAGS[currentLang] || FLAGS.it;
    document.querySelectorAll('.lang-switcher').forEach(function (sw) {
      var selected = sw.querySelector('.lang-selected');
      if (selected) selected.textContent = flag;
      sw.querySelectorAll('.lang-option').forEach(function (opt) {
        opt.style.display = opt.getAttribute('data-lang') === currentLang ? 'none' : '';
      });
      var dropdown = sw.querySelector('.lang-dropdown');
      if (dropdown) dropdown.classList.remove('open');
    });
  }

  window.toggleLangDropdown = function (btn) {
    var dropdown = btn.nextElementSibling;
    if (!dropdown) return;
    dropdown.classList.toggle('open');
  };

  document.addEventListener('click', function (e) {
    if (!e.target.closest || !e.target.closest('.lang-switcher')) {
      document.querySelectorAll('.lang-dropdown.open').forEach(function (d) {
        d.classList.remove('open');
      });
    }
  });

  function setLang(lang) {
    if (!T[lang]) return;
    currentLang = lang;
    localStorage.setItem('fashionai_lang', lang);
    applyTranslations();
    updateLangSwitchers();
    document.dispatchEvent(new CustomEvent('fashionai:langchange', { detail: { lang: lang } }));
  }

  /* Auto-init */
  function init() {
    applyTranslations();
    updateLangSwitchers();
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

  /* API pubblica */
  window.FashionAI = window.FashionAI || {};
  window.FashionAI.i18n = { t: t, setLang: setLang, getLang: function () { return currentLang; } };

  /* Funzione globale per i bottoni lingua nelle pagine */
  window.setLang = setLang;

  /* ── COOKIE BANNER ── */
  function initCookieBanner() {
    if (localStorage.getItem('cookie_accepted')) return;
    var lang = currentLang || 'it';
    var T2 = T[lang] || T['it'];
    var banner = document.createElement('div');
    banner.id = 'cookie-banner';
    var p = document.createElement('p');
    p.innerHTML = (T2.cookie_banner_text || '') + ' <a href="cookies.html">' + (T2.cookie_banner_link || '') + '</a>';
    var btn = document.createElement('button');
    btn.textContent = T2.cookie_banner_accept || 'OK';
    btn.addEventListener('click', function () {
      localStorage.setItem('cookie_accepted', '1');
      banner.remove();
    });
    banner.appendChild(p);
    banner.appendChild(btn);
    document.body.appendChild(banner);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initCookieBanner);
  } else {
    initCookieBanner();
  }

